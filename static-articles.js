這是一個特徵，但同時也是一個陷阱。

如果你無限地付出、無限地承擔，你會被家人的期待和依賴完全吃掉。這裡最難的功課是：如何在愛與界線之間找到平衡。你需要學會說「不」，需要學會讓家人為自己的人生負責，需要學會在關心和放手之間找到尺度。

很多靈修者在這一點上翻車，因為他們誤以為「靈修」就是無限奉獻。但真正的靈修，包括了學會如何在複雜的人倫關係中，既保持慈悲，又守住個人的邊界。

