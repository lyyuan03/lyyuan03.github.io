我見過太多人自欺欺人，說自己「感應到了」「差一點就靈動」「心裡有聲音」。但真正的靈動，不需要解釋，你自己會知道那是什麼——那是你的肉體不受你大腦控制的時刻，那是某種力量透過你的身體運作的直接體驗。它不是感覺，不是想像，不是催眠。它是現實。

所以別糾纏在「到底有沒有」這個問題上。靈動就像懷孕一樣——你要麼懷孕，要麼沒有，沒有人會因為「感覺上有點懷孕」而真的生出孩子。

既然你無法自欺欺人，那就老實問自己：我的靈動是真實的嗎？如果答案含糊，那就是沒有。

