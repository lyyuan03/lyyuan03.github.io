這也是為什麼，我見過的個案裡，真正走偏的人，往往不是能力最弱的人，而是內在傷得最深、卻又最急於證明自己不同凡響的人。氣脈給了他們一個出口，心魔就從那個出口鑽了出來，並且很快地，用「神旨」「天命」這些崇高的名字，把自己包裝起來。

